function parseArgs(argv) {
  const options = {
    input: DEFAULT_INPUT,
    output: null,
    write: false,
    check: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--help') {
      printHelp();
      process.exit(0);
    } else if (arg === '--write') {
      options.write = true;
    } else if (arg === '--check') {
      options.check = true;
    } else if (arg === '--input' || arg === '--output') {
      const value = argv[index + 1];
      if (!value || value.startsWith('--')) {
        throw new Error(`${arg} 缺少路径参数`);
      }
      options[arg.slice(2)] = path.resolve(process.cwd(), value);
      index += 1;
    } else {
      throw new Error(`未知参数：${arg}`);
    }
  }

  if (options.write && options.check) {
    throw new Error('--write 与 --check 不能同时使用');
  }
  if (options.output && !options.write) {
    throw new Error('--output 必须与 --write 同时使用');
  }
  options.output ??= options.input;
  return options;
}

function protectedEnglishView(record) {
  const clone = structuredClone(record);
  for (const field of ZH_FIELDS) {
    delete clone[field];
  }
  if (clone.instructions && typeof clone.instructions === 'object') {
    delete clone.instructions.zh;
  }
  if (clone.instruction_steps && typeof clone.instruction_steps === 'object') {
    delete clone.instruction_steps.zh;
  }
  return clone;
}

function assertDataset(records) {
  if (!Array.isArray(records)) {
    throw new Error('数据顶层必须是数组');
  }
  if (records.length !== 1324) {
    throw new Error(`记录数应为 1324，实际为 ${records.length}`);
  }

  const ids = new Set();
  for (const record of records) {
    if (!record || typeof record !== 'object' || Array.isArray(record)) {
      throw new Error('存在无效记录');
    }
    if (typeof record.id !== 'string' || record.id.length === 0) {
      throw new Error('存在缺少 ID 的记录');
    }
    if (ids.has(record.id)) {
      throw new Error(`存在重复 ID：${record.id}`);
    }
    ids.add(record.id);

    for (const field of ZH_FIELDS) {
      const value = record[field];
      if (field === 'secondary_muscles_zh') {
        if (!Array.isArray(value) || value.length === 0 || value.some((item) => typeof item !== 'string' || item.trim() === '')) {
          throw new Error(`ID ${record.id} 的 ${field} 为空或格式错误`);
        }
      } else if (typeof value !== 'string' || value.trim() === '') {
        throw new Error(`ID ${record.id} 的 ${field} 为空或格式错误`);
      }
    }

    if (!record.instructions || typeof record.instructions.zh !== 'string' || record.instructions.zh.trim() === '') {
      throw new Error(`ID ${record.id} 缺少中文训练步骤`);
    }
    if (!record.instruction_steps || !Array.isArray(record.instruction_steps.zh) ||
        record.instruction_steps.zh.length === 0 ||
        record.instruction_steps.zh.some((step) => typeof step !== 'string' || step.trim() === '')) {
      throw new Error(`ID ${record.id} 缺少分步中文训练说明`);
    }
  }

  for (const id of [...Object.keys(REVIEWED_FIXES), ...Object.keys(NAME_ONLY_FIXES)]) {
    if (!ids.has(id)) {
      throw new Error(`定点修正引用了不存在的 ID：${id}`);
    }
  }
}

function replaceAndCount(text, pattern, replacement) {
  let count = 0;
  const output = text.replace(pattern, (...args) => {
    count += 1;
    if (typeof replacement === 'function') {
      return replacement(...args);
    }
    return replacement.replace(/\$(\d+)/g, (_, group) => args[Number(group)] ?? '');
  });
  return { output, count };
}

function splitChineseInstructions(text) {
  const steps = text
    .match(/[^。！？]+[。！？]?/g)
    ?.map((step) => step.trim())
    .filter(Boolean) ?? [];
  if (steps.length === 0) {
    throw new Error('定点修正生成了空的中文步骤');
  }
  return steps;
}

function normalizeChineseName(name) {
  return name
    .replace(/坐式/g, '坐姿')
    .replace(/过头(?!顶)/g, '过顶')
    .replace(/(^|[^肱])二头肌/g, '$1肱二头肌')
    .replace(/(^|[^肱])三头肌/g, '$1肱三头肌')
    .replace(/三角肌后侧/g, '三角肌后束')
    .replace(/博苏球|BOSU球/g, 'BOSU 球')
    .replace(/V\s*(?:[型形])?(?:把(?:手)?|杆)/g, 'V 型把手')
    .replace(/T\s*(?:杆|[型形]杆)/g, 'T 杠')
    .replace(/反向\s*T\s*[型形]?杆?划船/g, '反向 T 杠划船')
    .replace(/D\s*形把手/g, 'D 型把手')
    .replace(/([BLVWY])形/g, '$1 形')
    .replace(/([BLTVWY])字/g, '$1 字')
    .replace(/([BLTVWY])式/g, '$1 式')
    .replace(/([\u3400-\u9fff球])([BDLTVWY])\s*(形|型|字|式|杠)/g, '$1 $2 $3')
    .replace(/([\u3400-\u9fff])JM\s*卧推/g, '$1 JM 卧推')
    .replace(/后侧平举/g, '后束平举')
    .replace(/斜托/g, '牧师凳')
    .replace(/SkiErg滑雪机/g, 'SkiErg 滑雪机')
    .replace(/sz-bar/gi, 'SZ 曲杆')
    .replace(/版本\s*(\d+)/g, '版本 $1')
    .replace(/(\d+)\s*度/g, '$1°')
    .replace(/([\u3400-\u9fff])(?=\d+°)/g, '$1 ')
    .replace(/(\d+°)(?=[\u3400-\u9fff])/g, '$1 ')
    .replace(/^(\d+\/\d+)(?=[\u3400-\u9fff])/g, '$1 ')
    .replace(/Pro背阔肌拉杆/g, 'Pro Lat Bar')
    .replace(/\s{2,}/g, ' ')
    .trim();
}

function normalizeChineseTerm(text) {
  return text
    .replace(/(^|[^肱])二头肌/g, '$1肱二头肌')
    .replace(/(^|[^肱])三头肌/g, '$1肱三头肌')
    .replace(/博苏球|BOSU球/g, 'BOSU 球')
    .replace(/稳定球/g, '健身球')
    .trim();
}

function applyContextualInstructionRules(record, input, ruleCounts) {
  let output = input;
  const apply = (label, pattern, replacement) => {
    const result = replaceAndCount(output, pattern, replacement);
    output = result.output;
    ruleCounts.set(label, (ruleCounts.get(label) ?? 0) + result.count);
  };

  // 绳索器械中的 bar 是附件拉杆，不是自由重量杠铃。
  if (record.equipment === 'cable') {
    apply('语境修正：绳索拉杆', /背阔肌杠铃|杠铃杆|杠铃/g, '拉杆');
  }

  // 英文步骤明确写 pull-up bar 时，中文统一为“单杠”。
  if (record.instructions.en?.includes('pull-up bar')) {
    apply('语境修正：pull-up bar', /杠铃杆|杠铃/g, '单杠');
  }
  if (new Set(['0466', '1401']).has(record.id)) {
    apply('语境修正：单杠动作', /杠铃杆/g, '单杠');
  }

  // 这些杠铃动作的中文步骤把通用 weights 误写成了哑铃。
  if (new Set(['0023', '2407', '0031', '0059', '0072', '0106', '0636']).has(record.id)) {
    apply('语境修正：杠铃动作', /哑铃/g, '杠铃');
  }

  // 药球动作不应把 medicine ball 写成健身球；0847 同时使用两种球，已定点重写。
  if (record.equipment === 'medicine ball' && record.id !== '0847') {
    apply('语境修正：药球', /健身球|实心球/g, '药球');
  }

  if (record.equipment === 'bosu ball') {
    apply('语境修正：BOSU 球', /博苏球|BOSU球/g, 'BOSU 球');
  }

  if (record.equipment === 'ez barbell') {
    apply('语境修正：EZ 曲杆', /(?:ez|EZ)\s*杠铃/g, 'EZ 曲杆');
  }

  if (record.target === 'biceps') {
    apply('语境修正：肱二头肌弯举', /(?:向上|向下)?卷向/g, '弯举至');
    apply('语境修正：肱二头肌弯举', /卷曲/g, '弯举');
  }

  // 英文动作名和 GIF 明确为反握，修正原中文步骤中的正握直译错误。
  if (new Set(['0118', '1317', '1330', '2327', '1351', '0758']).has(record.id)) {
    apply('语境修正：反握动作', /正握|反握（掌心朝下）/g, '反握');
    apply('语境修正：反握掌心', /手掌朝下|掌心朝下|掌心朝向身体/g, '掌心朝上');
  }

  // 英文名和动作图均明确为掌心相对，统一使用“中立握”。
  if (record.id === '1623') {
    apply('语境修正：掌心相对推举', /正握/g, '中立握');
  }

  return output;
}

function applyRefinements(records) {
  const snapshot = (record) => ({
    name_zh: record.name_zh,
    category_zh: record.category_zh,
    body_part_zh: record.body_part_zh,
    equipment_zh: record.equipment_zh,
    target_zh: record.target_zh,
    muscle_group_zh: record.muscle_group_zh,
    secondary_muscles_zh: structuredClone(record.secondary_muscles_zh),
    instructions_zh: record.instructions.zh,
    instruction_steps_zh: structuredClone(record.instruction_steps.zh),
  });
  const beforeById = new Map(records.map((record) => [record.id, snapshot(record)]));
  const ruleCounts = new Map(SAFE_INSTRUCTION_RULES.map(([label]) => [label, 0]));

  for (const record of records) {
    const reviewed = REVIEWED_FIXES[record.id];
    if (reviewed) {
      record.name_zh = reviewed.name_zh;
    } else if (NAME_ONLY_FIXES[record.id]) {
      record.name_zh = NAME_ONLY_FIXES[record.id];
    }

    record.name_zh = normalizeChineseName(record.name_zh);
    record.category_zh = normalizeChineseTerm(record.category_zh);
    record.body_part_zh = normalizeChineseTerm(record.body_part_zh);
    record.target_zh = normalizeChineseTerm(record.target_zh);
    record.muscle_group_zh = normalizeChineseTerm(record.muscle_group_zh);
    record.secondary_muscles_zh = record.secondary_muscles_zh.map((term, index) => {
      if (record.secondary_muscles?.[index] === 'groin') return '髋内收肌群';
      return normalizeChineseTerm(term);
    });

    if (record.equipment === 'assisted') record.equipment_zh = '辅助';
    if (record.equipment === 'bosu ball') record.equipment_zh = 'BOSU 球';
    if (record.equipment === 'ez barbell') record.equipment_zh = 'EZ 曲杆';
    if (record.equipment === 'leverage machine') record.equipment_zh = '固定器械';
    if (record.equipment === 'stepmill machine') record.equipment_zh = '爬楼机';
    record.equipment_zh = normalizeChineseTerm(record.equipment_zh);

    const sourceSteps = reviewed
      ? splitChineseInstructions(reviewed.instructions_zh)
      : record.instruction_steps.zh;
    const refinedSteps = sourceSteps.map((sourceStep) => {
      let step = sourceStep;
      const applySafeRules = () => {
        for (let pass = 0; pass < 4; pass += 1) {
          const beforePass = step;
          for (const [label, pattern, replacement] of SAFE_INSTRUCTION_RULES) {
            const result = replaceAndCount(step, pattern, replacement);
            step = result.output;
            ruleCounts.set(label, ruleCounts.get(label) + result.count);
          }
          if (step === beforePass) break;
        }
      };
      applySafeRules();
      step = applyContextualInstructionRules(record, step, ruleCounts).trim();
      applySafeRules();
      if (!/[。！？]$/.test(step)) step += '。';
      return step;
    });

    record.instruction_steps.zh = refinedSteps;
    record.instructions.zh = refinedSteps.join(' ');
  }

  const changes = [];
  for (const record of records) {
    const before = beforeById.get(record.id);
    const after = snapshot(record);
    const fields = [];
    for (const field of Object.keys(after)) {
      if (JSON.stringify(before[field]) !== JSON.stringify(after[field])) fields.push(field);
    }
    if (fields.length > 0) changes.push({ id: record.id, fields });
  }

  return { changes, ruleCounts };
}

async function atomicWrite(targetPath, contents) {
  const temporaryPath = `${targetPath}.tmp-${process.pid}`;
  await writeFile(temporaryPath, contents, 'utf8');
  await rename(temporaryPath, targetPath);
}

async function main() {
  const options = parseArgs(process.argv.slice(2));
  const source = await readFile(options.input, 'utf8');
  const records = JSON.parse(source.replace(/^\uFEFF/, ''));

  assertDataset(records);
  const protectedBefore = JSON.stringify(records.map(protectedEnglishView));
  const { changes, ruleCounts } = applyRefinements(records);
  assertDataset(records);
  const protectedAfter = JSON.stringify(records.map(protectedEnglishView));

  if (protectedBefore !== protectedAfter) {
    throw new Error('检测到英文或其他非中文字段发生变化，已中止');
  }

  const changedNameCount = changes.filter(({ fields }) => fields.includes('name_zh')).length;
  const changedInstructionCount = changes.filter(({ fields }) => fields.includes('instructions_zh')).length;
  const changedStepCount = changes.filter(({ fields }) => fields.includes('instruction_steps_zh')).length;
  const changedTaxonomyCount = changes.filter(({ fields }) =>
    fields.some((field) => !['name_zh', 'instructions_zh', 'instruction_steps_zh'].includes(field))).length;
  const activeRules = [...ruleCounts.entries()].filter(([, count]) => count > 0);

  console.log(`模式：${options.write ? '写入' : options.check ? '检查' : '预览（未写入）'}`);
  console.log(`记录数：${records.length}`);
  console.log(`发生变化的记录：${changes.length}`);
  console.log(`动作名修正：${changedNameCount}`);
  console.log(`中文步骤修正：${changedInstructionCount}`);
  console.log(`分步中文说明修正：${changedStepCount}`);
  console.log(`中文分类术语修正：${changedTaxonomyCount}`);
  console.log(`英文及其他非中文字段：未变化`);

  if (activeRules.length > 0) {
    console.log('全库安全规则命中：');
    for (const [label, count] of activeRules) {
      console.log(`  - ${label}: ${count}`);
    }
  }

  if (changes.length > 0) {
    const changedIds = changes.map(({ id }) => id);
    const visibleIds = changedIds.slice(0, 80).join(', ');
    const omittedCount = Math.max(0, changedIds.length - 80);
    console.log(`变更 ID：${visibleIds}${omittedCount > 0 ? ` …（另 ${omittedCount} 条）` : ''}`);
  }

  if (options.write) {
    const output = `${JSON.stringify(records, null, 2)}\n`;
    await atomicWrite(options.output, output);
    console.log(`已写入：${options.output}`);
  } else {
    console.log('未写入文件；确认结果后使用 --write。');
  }

  if (options.check && changes.length > 0) {
    process.exitCode = 1;
  }
}

main().catch((error) => {
  console.error(`中文数据精修失败：${error.message}`);
  process.exitCode = 1;
});
